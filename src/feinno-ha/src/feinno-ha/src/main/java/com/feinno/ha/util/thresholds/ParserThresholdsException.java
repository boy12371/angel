package com.feinno.ha.util.thresholds;

public class ParserThresholdsException extends Exception
{
	private static final long serialVersionUID = 9207388915932191547L;

	public ParserThresholdsException()
	{
		super();
	}

	public ParserThresholdsException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public ParserThresholdsException(String message)
	{
		super(message);
	}

	public ParserThresholdsException(Throwable cause)
	{
		super(cause);
	}
}
